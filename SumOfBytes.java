public class SumOfBytes {
    public static void main(String[] args) {
        byte b1 = 2, b2 = 4, b3 = 6, b4 = 8, b5 = 10, b6 = 12, b7 = 14, b8 = 16, b9 = 18, b10 = 20;
        int sum = b1 + b2 + b3 + b4 + b5 + b6 + b7 + b8 + b9 + b10;
        System.out.println("Sum of 10 bytes : " + sum);
    }
}