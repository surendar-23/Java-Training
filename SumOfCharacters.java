public class SumOfCharacters {
    public static void main(String[] args) {
        char c1 = 'a', c2 = 'B', c3 = 'c', c4 = 'D', c5 = 'e', c6 = 'F', c7 = 'g', c8 = 'H', c9 = 'i', c10 = 'J';
        int sum = c1 + c2 + c3 + c4 + c5 + c6 + c7 + c8 + c9 + c10;
        System.out.println("Sum of 10 characters: " + sum);
    }
}